// fungsi main(List<String> arguments) dibawah adalah fungsi level tinggi yang
// diperlukan untuk menjalankan kode-kode program Dart didalamnya.
void main(List<String> arguments) {
  // "Hello World" adalah standar awal belajar di semua bahasa pemrograman bagi
  // pemula, program Hello World pada dasarnya adalah mengeluarkan keluaran
  // berupa // teks "Hello, World!" untuk men-cek apakah setup/setting bahasa
  // pemrograman // sudah sesuai dan bisa berjalan untuk dikembangkan lebih
  // lanjut.

  // Untuk menghasilkan keluaran menggunakan fungsi print(object); dengan
  // memberikan sebuah value kedalam parameter bernama 'object' yang ada pada
  // fungsi print().

  // Contoh 1:
  // Menampilkan "Hello, World!".
  print('Hello, World!');

  // Contoh 2:
  // Menampilkan value dari sebuah variable bernama halo yang berisikan
  // "Halo, Dunia!"
  String halo = 'Halo, Dunia!';
  print(halo);

  // Contoh 3:
  // Menampilkan hasil aritmatika penambahan antara dua angka. Keluaran yang
  // dihasilkan kode dibawah yaitu : 15 (hasil dari penambahan 5 dengan 10).
  print(5 + 10);
}
